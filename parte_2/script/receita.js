const urlParams = new URLSearchParams(window.location.search);
const receitaId = urlParams.get('id');
const receita = dados.find(dado => dado.id === receitaId);

const class_ingredientes = document.querySelector('.ingredientes')
const class_preparo = document.querySelector('.preparo')
const class_img = document.querySelector('.receita-img')
const voltar = document.getElementById('voltar-receitas')

if (receita) {
    if(receita.id < 4){
        voltar.href = "./doces.html"
    }else{
        voltar.href = "./almoco-janta.html"
    }

    document.querySelector('.title-receita').textContent = receita.titulo;
    class_img.src = receita.img;
    receita.ingredientes.forEach(item => {
        var li = document.createElement("li");
        li.textContent = item
        class_ingredientes.appendChild(li)
    });
    receita.preparo.forEach(item => {
        var li = document.createElement("li");
        li.textContent = item
        class_preparo.appendChild(li)
    });
} else {
    document.body.innerHTML = '<p>Receita não encontrada!</p>';
}